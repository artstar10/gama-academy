var mes = "quero o mes de janeiro";

switch (mes) {
    case "janeiro":
        console.log("mês 1");
        break;
    case "fevereiro":
        console.log("mês 2");
        break;
    case "março":
        console.log("mês 3");
        break;
    default:
        console.log("nenhum dos casos acima foram atendidos");
        break;
}