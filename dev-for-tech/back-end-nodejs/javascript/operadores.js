// operadores aritméticos

// adição +
var somarDoisNumeros = 1 + 3;
console.log(somarDoisNumeros);
//subtração -
var subtrairDoisNumeros = 1 - 3;
console.log(subtrairDoisNumeros);
//multiplicação *
var multiplicarDoisNumeros = 1 * 3;
console.log(multiplicarDoisNumeros);
//divisão /
var dividirDoisNumeros = 4 / 2;
console.log(dividirDoisNumeros);
//módulo ou resto de uma divisão -
console.log(8%3);
// incremento ++
var numero = 1;
console.log(++numero);
numero = numero + 1
// decremento --
var numero = 2;
console.log(--numero);
numero = numero - 1
